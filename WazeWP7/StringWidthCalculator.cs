        static bool initialized = false;
        // FontSize -> (character, fontWidth)
        static Dictionary<int, Dictionary<char, int>> fontSizeToCharWidth = new Dictionary<int, Dictionary<char, int>>();

        public static void AddCharInfo(int fontSize, char c, int fontWidth)
        {
            Dictionary<char, int> charWidth;
            if (!fontSizeToCharWidth.TryGetValue(fontSize, out charWidth))
            {
                charWidth = new Dictionary<char, int>();
                fontSizeToCharWidth.Add(fontSize, charWidth);
            }

            charWidth.Add(c, fontWidth);
        }

        public static int CalcStringSize(string str, int fontSize)
        {
            if (!initialized)
            {
                initialized = true;
                init();
            }

            Dictionary<char, int> charWidth;
            if (!fontSizeToCharWidth.TryGetValue(fontSize, out charWidth))
                return (int) (fontSize * 0.56) * str.Length;

            int size = 0;
            int singleCharWidth;
            for (int i = 0; i < str.Length; i++)
            {
                if (!charWidth.TryGetValue(str[i], out singleCharWidth))
                {
                    size += (int) (fontSize * 0.56);
                }
                else
                {
                    size += singleCharWidth;
                }
            }
            if (str.Contains("ןולייא") || str.Contains("איילון"))
                size += 10;
            return size;
        }

